USE SHOPPING;

DROP TABLE STUDENTS;
DROP TABLE PAPERS;


CREATE TABLE STUDENTS(
	ID INT PRIMARY KEY AUTO_INCREMENT, 
	FIRST_NAME VARCHAR(20)
);

CREATE TABLE PAPERS(
	TITLE VARCHAR(50),
	GRADE INT,
	STUDENT_ID INT,
	FOREIGN KEY (STUDENT_ID) REFERENCES STUDENTS(ID) ON DELETE CASCADE
);

DESC STUDENTS;
DESC PAPERS;


