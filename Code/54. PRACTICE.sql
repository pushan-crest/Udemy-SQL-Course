-- there is a function named ISNULL()

-- THIS FUNCTION TAKES INPUT OF 
-- IFNULL(FIRST, SECOND);
-- IF FIRST IS NULL THEN SECOND

SELECT FIRST_NAME, IFNULL(AVG(GRADE), 0) FROM students
LEFT JOIN PAPERS ON PAPERS.STUDENT_ID = STUDENTS.ID
GROUP BY FIRST_NAME;


SELECT FIRST_NAME, IFNULL(AVG(GRADE), 0) FROM students
LEFT JOIN PAPERS ON PAPERS.STUDENT_ID = STUDENTS.ID
GROUP BY FIRST_NAME
ORDER BY 2 DESC; 

SELECT FIRST_NAME, IFNULL(AVG(GRADE), 0), 
	CASE
		WHEN IFNULL(AVG(GRADE),0) > 75 THEN 'PASSING'
		ELSE 'FAIL'
	END AS RESULTS
FROM students
LEFT JOIN PAPERS ON PAPERS.STUDENT_ID = STUDENTS.ID
GROUP BY FIRST_NAME
ORDER BY 2 DESC;
